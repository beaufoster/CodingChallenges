// Description:
// Messi goals function
// Messi is a soccer player with goals in three leagues:

// LaLiga
// Copa del Rey
// Champions
// Complete the function to return his total number of goals in all three leagues.

// Note: the input will always be valid.

// For example:

// 5, 10, 2  -->  17


const totalGoals = (Laliga, Copa, Champions) => Laliga + Copa + Champions

console.log(totalGoals(5, 10, 20), [17])
console.log(totalGoals(4 ,10, 33), [47])
console.log(totalGoals(1, 1, 1), [3])
